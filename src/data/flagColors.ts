/** Default confetti colors when no flag colors are available. */
const DEFAULT_COLORS = ['#5bb8d4', '#c41e3a', '#ffffff', '#ffd700']

/**
 * Extract ISO 3166-1 alpha-2 code from a flag emoji.
 * Flag emojis are pairs of Regional Indicator Symbols (U+1F1E6..U+1F1FF).
 */
function emojiToIso(emoji: string): string | null {
  const codePoints = [...emoji].map((c) => c.codePointAt(0) ?? 0)
  if (
    codePoints.length === 2 &&
    codePoints[0] >= 0x1f1e6 &&
    codePoints[0] <= 0x1f1ff &&
    codePoints[1] >= 0x1f1e6 &&
    codePoints[1] <= 0x1f1ff
  ) {
    return String.fromCharCode(
      codePoints[0] - 0x1f1e6 + 65,
      codePoints[1] - 0x1f1e6 + 65,
    )
  }
  return null
}

/** Get confetti colors for a mission entry's flag emoji. */
export function getFlagColors(emoji: string): string[] {
  const iso = emojiToIso(emoji)
  if (iso && FLAG_COLORS[iso]) return FLAG_COLORS[iso]
  return DEFAULT_COLORS
}
