// Precompute valid characters for code keyboard
export function getValidFirstChars(): Set<string> {
  return new Set(missions.map((m) => m.code[0]))
}

export function getValidSecondChars(firstChar: string): Set<string> {
  return new Set(
    missions.filter((m) => m.code[0] === firstChar).map((m) => m.code[1]),
  )
}

export function getValidCharsForPosition(
  position: 0 | 1,
  firstChar?: string,
): Set<string> {
  if (position === 0) return getValidFirstChars()
  if (position === 1 && firstChar) return getValidSecondChars(firstChar)
  return new Set()
}
